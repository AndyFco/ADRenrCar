     public void llenarTabla()
    {
        String []Datos = new String[4];
        try {
            Statement st =cn.createStatement();
            ResultSet rs= st.executeQuery("SELECT * FROM vehiculos"
                    + " WHERE Estado ='Disponible'");
            while (rs.next())
            {
                Datos[0]=rs.getString(1);
                Datos[1]=rs.getString(2);
                Datos[2]=rs.getString(3);
                Datos[3]=rs.getString(8);
                modeloV.addRow(Datos);
                
            }
            this.TablaVehiculos.setModel(modeloV);
        } catch (SQLException ex) {
            Logger.getLogger(CatalogoDeVehiculos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void Cambio(String placa)
    {

        String sql = "UPDATE vehiculos SET Estado ='Alquilado' "
                + "WHERE Placa='" + placa + "'";
        try {
            PreparedStatement st = cn.prepareStatement(sql);
            st.executeUpdate();
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    } 
