    public void AgregarFactura()
    {
        int filaseleccionada = TablaVehiculos.getSelectedRow();
        String placa = TablaVehiculos.getValueAt(filaseleccionada, 0).toString();
        
        try {
        PreparedStatement pst = cn.prepareStatement("INSERT INTO factura("
                + "Nombre_Cliente,Paquete,FechadeSalida,FechadeRegreso,Seguro"
                + ",Placa,Usuario)VALUES (?,?,?,?,?,?,?)");
        pst.setString(1, (String)BoxClientes.getSelectedItem());
        pst.setString(2, (String)BoxPaquete.getSelectedItem());
        pst.setString(3, jFechadeSalida.getDate().toString());
        pst.setString(4, jFechadeRegreso.getDate().toString());
        pst.setString(5, (String)BoxSeguro.getSelectedItem());
        pst.setString(6, placa);
        pst.setString(7, usuario);
        pst.executeUpdate();
        JOptionPane.showMessageDialog(null,"Realizado");
        Cambio(placa);
    } catch (SQLException e) {
        System.out.print(e.getMessage());
    }
     }
