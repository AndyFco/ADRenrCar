    public void Cambio(String usuario, String pass)
    {
        if(pass2.getText().equals(PassN.getText()))
        {
            String sql = "UPDATE usuarios set pass ='"+ pass2.getText() +"'"
            + " WHERE NombreU='" + usuario + "' && pass='" + pass + "'";
            try {
                PreparedStatement st = cn.prepareStatement(sql);
                st.executeUpdate();
                JOptionPane.showMessageDialog(this, "Actualizado");
            } catch (SQLException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, ex);
            }
        }else JOptionPane.showMessageDialog(this, "elnuevo pass no coincide");
    }
